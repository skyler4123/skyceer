require "rails_helper"

RSpec.describe DemosController, type: :routing do
  describe "routing" do
    it "routes to #index" do
      expect(get: "/demos").to route_to("demos#index")
    end

    it "routes to #new" do
      expect(get: "/demos/new").to route_to("demos#new")
    end

    it "routes to #show" do
      expect(get: "/demos/1").to route_to("demos#show", id: "1")
    end

    it "routes to #edit" do
      expect(get: "/demos/1/edit").to route_to("demos#edit", id: "1")
    end


    it "routes to #create" do
      expect(post: "/demos").to route_to("demos#create")
    end

    it "routes to #update via PUT" do
      expect(put: "/demos/1").to route_to("demos#update", id: "1")
    end

    it "routes to #update via PATCH" do
      expect(patch: "/demos/1").to route_to("demos#update", id: "1")
    end

    it "routes to #destroy" do
      expect(delete: "/demos/1").to route_to("demos#destroy", id: "1")
    end
  end
end
